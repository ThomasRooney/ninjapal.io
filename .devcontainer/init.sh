reach sound, actionable decisions.
"""
EOF

# Ensure at least one API key is configured
if [ -z "$GEMINI_API_KEY" ] && [ -z "$OPENAI_API_KEY" ]; then
    echo "WARNING: No API keys found. Zen MCP server requires at least one API key."
    echo "Please set GEMINI_API_KEY or OPENAI_API_KEY in your environment."
    exit 0  # Exit gracefully without failing the container startup
else
    # Run the first-time-setup installation with automatic yes to prompts
    echo "Running zen MCP server setup..."
    if yes Y | ./run-server.sh; then
        echo "Zen MCP server setup completed successfully."
    else
        echo "WARNING: Zen MCP server setup encountered issues but continuing..."
        # Log the error but don't fail the container startup
    fi
    
    # Give appropriate permissions to vscode user
    chown -R vscode:vscode /opt/zen-mcp-server
fi

echo "Zen MCP server installation complete."
echo 'export PATH="/usr/local/devcontainer/bin:$PATH"' | sudo -u vscode tee -a /home/vscode/.bashrc > /dev/null
echo 'export PATH="/usr/local/devcontainer/bin:$PATH"' | sudo -u vscode tee -a /home/vscode/.zshrc > /dev/null
