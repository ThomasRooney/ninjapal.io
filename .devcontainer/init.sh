#!/bin/bash


su -l vscode -s /bin/bash -c "
    npm install -g @anthropic-ai/claude-code
    cd /workspace
    /home/vscode/.local/bin/mise trust
    /home/vscode/.local/bin/mise install
"

CLAUDE_LOCATION=$(su -l vscode -s /bin/bash -c 'which claude')

mkdir -p /usr/local/devcontainer/bin
echo '#!/bin/bash' > /usr/local/devcontainer/bin/claude-wrapper
printf "%s" "$CLAUDE_LOCATION" >> /usr/local/devcontainer/bin/claude-wrapper
echo ' --dangerously-skip-permissions "$@"' >> /usr/local/devcontainer/bin/claude-wrapper
chmod +x /usr/local/devcontainer/bin/claude-wrapper
ln -sf /usr/local/devcontainer/bin/claude-wrapper /usr/local/devcontainer/bin/claude

# Install zen MCP server
echo "Installing zen MCP server..."

# Clone the repository if it doesn't exist
if [ ! -d "/opt/zen-mcp-server" ]; then
    # Clone and checkout a specific tag for reproducibility
    git clone https://github.com/BeehiveInnovations/zen-mcp-server.git /opt/zen-mcp-server
    cd /opt/zen-mcp-server
    # Pin to latest stable tag (update as needed)
    git checkout tags/v0.1.0 2>/dev/null || echo "Warning: Could not checkout v0.1.0, using main branch"
else
    cd /opt/zen-mcp-server
fi

# Create .env file with API keys from environment
cat > .env << EOF
# API Keys Configuration
# Generated by devcontainer init script
DEFAULT_THINKING_MODE_THINKDEEP=high
EOF

# Add Gemini API key if available
# Note: GEMINI_API_KEY from environment is mapped to GOOGLE_API_KEY as expected by the Gemini API
if [ ! -z "$GEMINI_API_KEY" ]; then
    echo "GOOGLE_API_KEY=$GEMINI_API_KEY" >> .env
fi

# Add OpenAI API key if available  
if [ ! -z "$OPENAI_API_KEY" ]; then
    echo "OPENAI_API_KEY=$OPENAI_API_KEY" >> .env
    echo "OPENAI_ALLOWED_MODELS=o4-mini" >> .env
fi

# Modify the chat system prompt
cat > /opt/zen-mcp-server/systemprompts/chat_prompt.py <<EOF
"""
Chat tool system prompt
"""

