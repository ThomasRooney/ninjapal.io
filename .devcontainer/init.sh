who has context on the codebase: if it changes too much without being asked to, then it's messing up. It's your job to notice this, avoid it getting rabbit holed, 
as an example with git diff being too big or plan too ambiguous. If the plan is overly generic than it hasn't thought it through properly, you want precision out of it.

